export let language = storageGet('spinnerLanguage', 'en');

export function setLanguage(lang) {
  language = lang;
  storageSet('spinnerLanguage', lang);
}

export function t(key) {
  return (i18n[language] && i18n[language][key]) || i18n.en[key] || key;
}

export function formatStatus(key, params) {
  if (key === 'recordingStart') return (language === 'fi' ? 'Tallennetaan WebM ' : 'Recording high-quality WebM at ') + params.mbps + ' Mbps ' + (language === 'fi' ? params.seconds + 's...' : 'for ' + params.seconds + 's...');
  if (key === 'recordingFrames') return (language === 'fi' ? 'Tallennetaan WebM... ' : 'Recording WebM... ') + params.done + '/' + params.total + (language === 'fi' ? ' ruutua' : ' frames');
  if (key === 'webmSaved') return (language === 'fi' ? 'WebM tallennettu: ' : 'WebM saved: ') + params.seconds + 's / ' + params.fps + ' FPS. ' + (language === 'fi' ? 'Ääni sisältyy, jos selain salli sen.' : 'Sound is included if your browser allowed it.');
  if (key === 'gifFrames') return (language === 'fi' ? 'Tehdään GIF-ruutuja... ' : 'Making GIF frames... ') + params.done + '/' + params.total;
  return t(key);
}

export function applyLanguage() {
  document.documentElement.lang = language;
  document.title = t('appTitle');
  $('languageSelect').value = language;
  document.querySelectorAll('[data-i18n]').forEach(el => {
    el.textContent = t(el.dataset.i18n);
  });
  const statusKey = $('status').dataset.statusKey || 'ready';
  const params = $('status').dataset.statusParams ? JSON.parse($('status').dataset.statusParams) : null;
  $('status').textContent = formatStatus(statusKey, params);
  import('../controls/status.js').then(m => m.updatePauseBtnText());
  import('../controls/position.js').then(m => m.updateMeta());
}
